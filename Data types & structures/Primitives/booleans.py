# Booleans may have one of two values, True or False: 
print(type(True)) # <class 'bool'>
print(type(False)) # <class 'bool'>

# A value that is true in Boolean context is sometimes said to be "truthy", and
# one that is false in Boolean context is said to be "falsy". The
# 'truthiness..' of an object of Boolean type is self-evident: Boolean objects
# that are equal to True are truthy, and those equal to False aare falsy. But
# non-Boolean objects can be evaluated in Boolean context as well and
# determined to be true or false. 
